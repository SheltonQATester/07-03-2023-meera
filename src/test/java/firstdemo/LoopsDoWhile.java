package firstdemo;

public class LoopsDoWhile {


	//1----->10000

	int num=1;


	public void demoLoopProgram()
	{


		do
		{

			System.out.println(num);
			num++;

		}while(num<=1000);



	}


	public static void main(String[] args)
	{

		LoopsDoWhile l = new LoopsDoWhile();
		l.demoLoopProgram();

	}







}
